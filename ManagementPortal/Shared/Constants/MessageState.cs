namespace ManagementPortal.Shared.Constants
{
    /// <summary>
    /// Enumerado para identificar los estados de un mensaje.
    /// </summary>
    public enum MessageState
    {
        /// <summary>
        /// Estado de mensaje enviado.
        /// </summary>
        Send,

        /// <summary>
        /// Estado de mensaje recivido.
        /// </summary>
        Received,

        /// <summary>
        /// Estado de mensaje leido.
        /// </summary>
        Read,
    }
}